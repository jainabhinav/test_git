  def apply(
    context: Context,
    in0:     DataFrame,
    in1:     DataFrame,
    in2:     DataFrame,
    in3:     DataFrame,
    in4:     DataFrame,
    in5:     DataFrame
  ): DataFrame =
    in0
      .as("in0")
      .join(in1.as("in1"),
            col("in0.auction_id_64") === col("in1.auction_id_64"),
            "left_outer"
      )
      .join(in2.as("in2"),
            col("in1.auction_id_64") === col("in2.auction_id_64"),
            "left_outer"
      )
      .join(in3.as("in3"),
            col("in2.auction_id_64") === col("in3.auction_id_64"),
            "left_outer"
      )
      .join(in4.as("in4"),
            col("in3.auction_id_64") === col("in4.auction_id_64"),
            "left_outer"
      )
      .join(in5.as("in5"),
            col("in4.auction_id_64") === col("in5.auction_id_64"),
            "left_outer"
      )
      .select(
        col("in0.auction_id_64").as("auction_id_64"),
        col("in0.date_time").as("date_time"),
        agg_platform_video_requests(context).as("agg_platform_video_requests"),
        agg_dw_video_events(context).as("agg_dw_video_events"),
        agg_platform_video_impressions(context)
          .as("agg_platform_video_impressions"),
        agg_dw_clicks(context).as("agg_dw_clicks"),
        agg_dw_pixels(context).as("agg_dw_pixels"),
        agg_impbus_clicks(context).as("agg_impbus_clicks")
